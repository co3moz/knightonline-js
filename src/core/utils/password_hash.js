module.exports = function hash(password) {
  var buffer = Buffer.from(password, 'utf8');
  var inBytes = Array.from(buffer);
  var outBytes = [];
  var counter = 0, tmp = 0, inputKey = 0, outHash = 0;

  while (inBytes.length % 4 != 0) inBytes.push(0);

  for (var i = 0; i < inBytes.length; i += 4) {
    let encoded = (inBytes[i] + (inBytes[i + 1] << 8) + (inBytes[i + 2] << 16) + (inBytes[i + 3] << 24)) >>> 0;
    tmp = (encoded + 0x03e8) >>> 0;
    inputKey = tmp;
    counter = 0;
    outHash = 0;

    do {
      tmp = inputKey;

      inputKey = inputKey >>> 1;
      if (tmp % 2 != 0) {
        tmp = encodingArray[counter / 4 >>> 0];
        outHash = (outHash + (((1 << (tmp | 0)) >>> 0))) >>> 0;
      }
      counter = (counter + 4) | 0;
    } while (inputKey > 0);

    var tmpPut = new long(outHash.toString());
    for (var tmpInt = 0; tmpInt < 7; tmpInt++) {
      var upper = tmpPut.mul(954437177).shiftRight(35);
      var anotherTmp = upper.mul(8).add(upper).shiftLeft(2).low >>> 0;
      var difference = (tmpPut.sub(anotherTmp.toString())).low >>> 0;
      outBytes.push(alphabetArray[difference]);
      tmpPut = upper;
    }
  }

  return Buffer.from(outBytes, 'utf8').toString('utf8');
}